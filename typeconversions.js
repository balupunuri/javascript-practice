/*type conversion*/
/*number to string*/
let a = 45;
console.log(a);
console.log(typeof a);
let b = String(a);
console.log(b);
console.log(typeof b);

/*number to boolean*/
let y = 10;
console.log(y);
console.log(typeof y);
let x = Boolean(y);
console.log(x);
console.log(typeof x);

/*string to number*/
let num = "100";

let result = Number(num);

console.log(result);
console.log(typeof result);

console.log(10 == "10");
console.log(10 === "10");